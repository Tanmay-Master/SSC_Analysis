def get_marathi_name(english_name):
    """Get Marathi translation of school name"""
    return SCHOOL_MARATHI_MAPPING.get(english_name, english_name)

def get_school_display_name(english_name, show_marathi=True):
    """Get school name in both languages if show_marathi=True"""
    if show_marathi:
        marathi_name = get_marathi_name(english_name)
        if marathi_name != english_name:
            return f"{marathi_name}"  # Only Marathi
        return english_name
    return english_name
